
public class Soda {
	
	private String name;
	private int quanity;
	
	public Soda (String name, int quanity) {
		this.name = name;
		this.quanity = quanity;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getQuanity() {
		return quanity;
	}

	public void setQuanity(int quanity) {
		this.quanity = quanity;
	}

}
